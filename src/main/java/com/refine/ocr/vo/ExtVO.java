package com.refine.ocr.vo;

public class ExtVO {
    private String docFlExtNo;
    private String extType;
    private String extValue;
    private String extDesc;
    
    // Getters and setters
    public String getDocFlExtNo() { return docFlExtNo; }
    public void setDocFlExtNo(String docFlExtNo) { this.docFlExtNo = docFlExtNo; }
    
    public String getExtType() { return extType; }
    public void setExtType(String extType) { this.extType = extType; }
    
    public String getExtValue() { return extValue; }
    public void setExtValue(String extValue) { this.extValue = extValue; }
    
    public String getExtDesc() { return extDesc; }
    public void setExtDesc(String extDesc) { this.extDesc = extDesc; }
}